#Iterating over a list: 
fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)

# Iterating over a tuple: 
numbers = (1, 2, 3, 4, 5)

for num in numbers:
    print(num)

# Iterating over a string:
message = "Hello, world!"

for char in message:
    print(char)

# Iterating over a dictionary:
person = {"name": "John", "age": 30, "city": "New York"}

for key, value in person.items():
    print(key, ":", value)


# Iterating over a set:

fruits = {"apple", "banana", "cherry"}

for fruit in fruits:
    print(fruit)

# Using the range() function for numerical iteration:

for num in range(10):
    print(num)

for num in range(2, 10):
    print(num)

# Nested loops:
for i in range(3):
    for j in range(2):
        print(i, j)


