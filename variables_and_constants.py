# Variables example
name = "Alice"
age = 30
age = age + 1

# Constants example (note: Python doesn't enforce constants, but we use uppercase names for clarity)
SPEED_OF_LIGHT = 299792458
GRAVITY = 9.81
distance = SPEED_OF_LIGHT * 1.0

# Printing variables and constants
print("Name:", name)
print("Age:", age)
print("Speed of Light:", SPEED_OF_LIGHT)
print("Gravity:", GRAVITY)
print("Distance Light Travels in 1 Second:", distance)
